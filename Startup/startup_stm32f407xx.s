; Reset handler
Reset_Handler    PROC	;伪指令
                 EXPORT  Reset_Handler                    [WEAK] ;声明该标号为全局标号
        IMPORT  SystemInit	;该标号来自外部文件
        IMPORT  __main
                 LDR     R0, =0xE000ED88    ; 使能浮点运算 CP10,CP11
                 LDR     R1,[R0]			;加载字
                 ORR     R1,R1,#(0xF << 20)		
                 STR     R1,[R0]
                 LDR     R0, =SystemInit
                 BLX     R0					;跳转返回
                 LDR     R0, =__main
                 BX      R0					;跳转不返回，进入main函数
                 ENDP

